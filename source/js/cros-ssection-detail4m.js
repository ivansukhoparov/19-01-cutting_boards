const detailItemTemplate = document.querySelector('#detail-item').content.querySelector('.detail__item');


const readyContent = document.createDocumentFragment();


const clearContentPlace = function () {
  contentPlace.innerHTML = '';
};



const renderData = function (content) {
  //Cross-section - workpiece - detail - mark/length  content.woodDetails[0][2][0][1]
  const projectName = content.projectName;
  const woodCrossSections = content.woodCrossSections;
  const woodDescriptions = content.woodDescriptions;
  const woodDetails = content.woodDetails;

  clearContentPlace();

  woodDetails.forEach((element, index) => {
    const pieceCount = element.length;
    const crossSectionBlock = crossSectionBlockTemplate.cloneNode(true);
    crossSectionBlock.querySelector('.cross-section__value').textContent = woodCrossSections[index];
    //crossSectionBlock.querySelector('.cross-section__description').textContent = woodDescriptions[index];
    crossSectionBlock.querySelector('.cross-section__description').textContent = pieceCount + " шт";

    element.forEach((element, index) => {
      const detailNumber = index + 1;
      const worckPiece = worckPieceTemplate.cloneNode(true);
      let leavingLength = 4000;

      element.forEach((element) => {

        const detailItem = detailItemTemplate.cloneNode(true);
        const mark = element[0];
        const length = element[1];

        leavingLength = leavingLength - length
        detailItem.querySelector('.datail__number').textContent = detailNumber;
        detailItem.querySelector('.detail__mark').textContent = mark;
        detailItem.querySelector('.detail__length').textContent = length + ' mm';
        detailItem.setAttribute("style", "width:" + ((length / 10 * 3) - 2) + "px");

        worckPiece.querySelector('.detail__list').appendChild(detailItem);
      })


      worckPiece.querySelector('.detail__leaving').textContent = 'leaving: ' + leavingLength + ' mm'
      crossSectionBlock.querySelector('.cross-section__list').appendChild(worckPiece);
    })

    readyContent.appendChild(crossSectionBlock);
  })

  contentPlace.appendChild(readyContent);

};

